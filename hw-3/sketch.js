// declaring the setup function
function setup() {
  // creating a canvas 600px wide and 400px high
  createCanvas(600, 400);
  // coloring the bg 'pink'
  background('pink');
}

// declaring draw function
function draw() {
  // telling p5 to print the string "Hello World!"
  // positioned at (x: 20, y: 30)
  text( "Hello World!", 20, 30 );
}
